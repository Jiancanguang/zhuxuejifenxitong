/**
 * 获取字符串的拼音首字母
 * @param str 中文字符串
 * @returns 拼音首字母字符串（大写）
 */
export function getPinyinInitials(str: string): string {
    return str
        .split('')
        .map(char => {
            // 如果是英文字母，直接返回大写
            if (/[a-zA-Z]/.test(char)) {
                return char.toUpperCase();
            }
            // 如果在映射表中，返回首字母
            if (PINYIN_MAP[char]) {
                return PINYIN_MAP[char];
            }
            // 其他字符原样返回
            return char;
        })
        .join('');
}

/**
 * 检查名字是否匹配搜索词（支持拼音首字母和直接匹配）
 * @param name 学生姓名
 * @param searchTerm 搜索词
 * @returns 是否匹配
 */
export function matchesSearch(name: string, searchTerm: string): boolean {
    if (!searchTerm.trim()) return true;

    const term = searchTerm.toUpperCase();
    const upperName = name.toUpperCase();

    // 直接匹配姓名
    if (upperName.includes(term)) return true;

    // 匹配拼音首字母
    const initials = getPinyinInitials(name);
    if (initials.includes(term)) return true;

    return false;
}

