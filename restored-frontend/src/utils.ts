/**
 * 生成测试学生姓名列表
 * 混合两个字和三个字的真实姓名，不带序号
 * @param count 生成数量
 * @returns 姓名数组
 */
export function generateTestStudentNames(count: number): string[] {
    const names: string[] = [];
    const usedNames = new Set<string>();

    for (let i = 0; i < count; i++) {
        let name: string;
        let attempts = 0;

        do {
            const surname = SURNAMES[Math.floor(Math.random() * SURNAMES.length)];

            // 70%概率用双字名（三个字的名字），30%概率用单字名（两个字的名字）
            if (Math.random() < 0.7) {
                const givenName = DOUBLE_NAMES[Math.floor(Math.random() * DOUBLE_NAMES.length)];
                name = surname + givenName;
            } else {
                const givenName = SINGLE_NAMES[Math.floor(Math.random() * SINGLE_NAMES.length)];
                name = surname + givenName;
            }

            attempts++;
            if (attempts > 100) {
                // 防止无限循环，加上索引区分
                name = surname + DOUBLE_NAMES[i % DOUBLE_NAMES.length];
                break;
            }
        } while (usedNames.has(name));

        usedNames.add(name);
        names.push(name);
    }

    return names;
}

